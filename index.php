      <p>การเป็นเกย์และกระเทยส่วนหนึ่งมาจาก อิทธิพลของฮอร์โมนเพศตั้งแต่อยู่ในครรภ์มารดา นอกนั้นคือสิ่งแวดล้อม สังคมและการเลี้ยงดูในครอบครัว</p>
      <p>เพศที่ 3 กับสังคม</p>
      <p>คนทั่วไปส่วนใหญ่ยังมีความสับสนเรื่องผู้ชายด้วยกันหรือผู้ชายที่มี กิริยามารยาทคล้ายกับผู้หญิงนอกจากคนในสังคมจะสับสนและไม่เข้าใจแล้ว ยังมองบุคคลเหล่านี้ไปในทางลบ 
        ทั้งนี้จะต้องทำความเข้าใจก่อนว่าผู้ชายมีหลักๆ 3 กลุ่มคือ</p>
      <ul>
        <li>กลุ่มผู้ชายที่รักผู้หญิง (เพศตรงข้าม-ต่างเพศ) ซึ่งสังคมทั่วไปยอมรับ</li>
        <li>กลุ่มผู้ชายที่รู้สึกว่าตัวเองเป็นผู้ชาย แค่รักผู้ชายด้วยกัน (เพศเดียวกัน-ร่วมเพศ) ซึ่งมักนิยามตัวเองว่า เกย์ (gay) มาจากรากศัพท์ภาษาอังกฤษที่แปลวา สดใส ร่าเริง</li>
        <li>ผู้ชายที่มีจิตใจเป็นผู้หญิง หรือเป็นผู้หญิงที่อยู่ในร่างของผู้ชายนั่นเอง คำไทยมักใช้ว่า “กะเทย” (transgender หรือ transsexual)</li>
      </ul>
      <p> การที่ผู้ชายมีความรักเพศเดียวกัน มีสาแหตุหลายอย่าง ทั้งปัจจัยทางชีววิทยา และจิตวิทยา เริ่มตั้งแต่สาเหตุทางพันธุกรรม การพัฒนาของสมองเด็กในครรภ์ที่อยู่ภายใต้อิทธิพลของฮอร์โมนเพศ ตลอดจนการอบรมเลี้ยงดู และประสบการณ์การเรียนรู้หลังจากที่เกิดมาแล้วซึ่งพฤติกรรมเหล่านี้ไม่ได้ เกิดจากการเลียนแบบ และไม่ติดต่อกัน</p>
      <p> ทั้งเกย์และกระเทย จะเริ่มรู้สึกว่าตนเองต่างจากเพื่อเพศเดียวกัน ตั้งแต่อายุ 3-4 ขวบ หรือเมื่อเริ่มจำความได้ โดยจะจดจำว่าพ่อแม่ตักเตือนอยู่เสมอว่าอย่าทำตัวเป็นผู้หญิง ซึ่งอาจจะสร้างความรู้สึกกดดันภายในจิตใจ</p>
      <p>ด้านพฤติกรรมจะมีความรู้สึกอ่อนไหว ร้องไห้ง่าย กิริยามารยาทคล้ายเด็กหญิง ชอบแต่งตัว ชอบเล่นกับกลุ่มเพื่อนผู้หญิง ไม่ชอบเล่นรุนแรง เมื่อเริ่มเข้าสู่วัยรุ่นจะมีความรู้สึกทางเพศเดียวกัน แตกต่างจากเพื่อนผู้ชายทั่วไป</p>
      <p>  เนื่องจากเด็กรับรู้ว่าสังคมทั่วไปมีความรู้สึกรังเกียจเกย์และกระเทย เด็กจึงสับสนไม่แน่ใจ ในการวางตัวในสังคม ความเครียด และพยายามบิดปังความรู้สึกของตนเอง บางคนพยายามป้องกันตนเอง โดยพยายามทำตัวเป็นชายชาตรี เช่น พยายามมีคนรักเป็นผู้หญิงหลายๆ คน เพาะกายให้ดูเป็น “แมน” แสดงตนก้าวร้าว ดื่มเหล้า สูบบุหรี่ มีเด็กบางคนก็พยายามหาสิ่งทดแทนความด้อย เช่น พยายามขยัน ตั้งใจเรียน เอื้อเฟื้อเผื่อแผ่เพื่อน ซึ่งเป็นการทดแทนที่ดีถ้าพ่อแม่ผู้ปกครองและครูไม่เข้าใจ เด็กจะยิ่งมีความทุกข์ทรมานใจมากขึ้น บางคนมาปรึกษาแพทย์เพื่อขอฉีดฮอร์โมนเพศชาย โดยหวังว่าฮอร์โมนเพศชายจะช่วยเปลี่ยนความรู้สึกและความต้องการในใจ แต่ความเป็นจริงฮอร์โมนเหล่านี้ไม่ได้มีผลดังที่หวังเลย</p>
      <p> ระยะสับสนกังวลนี้อาจจะกินเวลานานมากหรือน้อย แล้วแต่ตัวเด็กแต่ละคน เมื่อผ่านระยะนี้ไปเด็กจะเริ่มยอมรับความรู้สึกและความต้องการทางเพศของตน เองมากขึ้น ความเครียด ความวิตกกังวลจะลดลง ในขั้นต่อไปอาจพัฒนาถึงขั้นเปิดเผยตนเองต่อสังคม และสามารถปรับตัวเข้ากับสังคมได้ดี</p>
      <p>  สังคมไทยยอมรับเกย์และกระเทยมากขึ้นกว่าในหลายประเทศ แต่คนบางกลุ่มก็ยังรู้สึกในแง่ลบกับเกย์และกะเทย ทั้งนี้อาจเป็นเพราะภาพเกย์และกะเทยที่ออกมาตามสื่อต่างๆ ทั้งหนังสือพิมพ์ละครโทรทัศน์ ภาพยนตร์ แสดงเป็นตัวตลกบ้าๆ บอๆ มีอารมณ์รุนแรงโหดเหี้ยม ซึ่งความจริงที่ปรากฏก็คือเกย์และกะเทยที่ประสบความสำเร็จในชีวิต เป็นคนดี ทำประโยชน์ให้สังคมก็มีอยู่มากชีวิตชายรักชาย</p>
      <p> ผลจากการศึกษากลุ่มชายรักชาย 100 คน ซึ่งเป็นเกย์ 95 คน เป็นกะเทย 5 คน พบว่า 40 คน ทำงานเป็นลูกจ้างในสำนักงาน รองลงมาคือ ธุรกิจส่วนตัว สถานเสริมความงาม ร้านอาหาร ร้องเพลง ครูเต้นรำ นาฏศิลป์ และพิธีกร ตามลำดับ</p>
      <p>ในจำนวนนี้ 61 คน มีความสัมพันธ์ทางเพศครั้งแรกกับผู้ชาย และจำนวน 34 คน มีความสัมพันธ์ทางเพศครั้งแรกกับผู้หญิง ผู้เข้ารับการสำรวจยังบอกว่ามีประสบการณ์ทางเพศหรือมีความสัมพันธ์ทางเพศ ส่วนใหญ่เนื่องจากความรัก หรือความต้องการทางเพศ และอีก 5 คนยังไม่เคยมีเพศสัมพันธ์</p>
      <p>ด้านชีวิตการแต่งงานพบว่า 88 คน ยังเป็นโสดหรือมีคู่เป็นชาย และอีก 12 คน เคยแต่งงานกับหญิง   ในจำนวนทั้งหมดนี้ 4 คน หย่ากับภรรยาแล้วเพราะ “เข้ากันไม่ได้”  อีก 2 คน หย่ากับภรรยาทั้งที่ครอบครัวมีความสุขพอควร แต่ยอมเสียสละให้ภรรยาได้มีโอกาสมีสามีที่เป็นชายเต็มตัว  อีก 1 คน กำลังจะหย่ากับภรรยา เพราะพบแฟนเป็นชายที่ให้ความสุขได้มากกว่าภรรยา  ส่วนอีก 5 คน ยังอยู่กับภรรยาและครอบครัวอย่างมีความสุข</p>
      <p>จากการสำรวจพบข้อสังเกตว่าโอกาสล้มเหลวของเกย์แต่งงานกับผู้หญิงมี ค่อนข้างมาก แต่ก็มีปัจจัยหลายอย่างที่อาจช่วยให้ชีวิตแต่งงานประสบความสำเร็จ คือรสนิยมทางเพศต้องเอียงไปทางชอบผู้หญิงมากกว่าผู้ชาย หรือแต่งงานกันด้วยความรักและความเข้าใจ มีความซื่อสัตย์มั่นคงไม่นอกใจภรรยา มีความรับผิดชอบต่อตนเองและครอบครัว รวมทั้งภรรยามีความเข้าใจยอมรับ และให้อภัยด้วย</p>
      <p>เรื่องความเสี่ยงต่อโรคเอดส์ พบว่ามี 69 คนที่ตรวจเลือดหาโรคเอดส์ มีผลการตรวจเป็นบวก (ติดเชื้อเอชไอวี-เอดส์) 5 ราย หรือร้อยละ 7.6 ซึ่งนับว่าสูงกว่าประชากรทั่วไป สำหรับเรื่องของการป้องกันหรือการดูแลสุขภาพร่างกายหลังจากติดเชื้อเอดส์ แล้วทางหน่วยงานที่มีส่วนในการรับผิดชอบควรมีมาตรการช่วยเหลือให้มากขึ้น
      เพศที่ 3 กับการแปลงเพศ</p>
      <p>สำหรับชายใจหญิงหรือกะเทยหลายคนที่มีความต้องการเปลี่ยนแปลงตนเองให้เหมือนผู้หญิงให้มากที่สุดนั่นคือเลือกผ่าตัดแปลงเพศ</p>
      <p>ประเทศไทยมีศัลยแพทย์ที่เชี่ยวชาญผ่าตัดแปลงเพศและเป็นที่ยอมรับกันทั่ว โลกหลายท่าน นอกจากคนไทยที่ขอเข้ารับการผ่าตัดปลงเพศแล้วยังมีจากประเทศอื่นๆ เช่น แคนาดา สหรัฐอเมริกา อังกฤษ สวีเดน บราซิล</p>
      <p>การผ่าตัดแปลงเพศต้องผ่านขั้นตอนและกฎเกณฑ์ต่างๆ หลายอย่าง เพราะเมื่อทำการผ่าตัดเปลี่ยนเป็นเพศหญิงแล้วจะเปลี่ยนกลับมาเป็นเพศชายที่ สมบูรณ์อีกไม่ได้ง่ายๆ โดยผู้เข้ารับการผ่าตัดแปลงเพศจะต้องมีอายุ 30-65 ปี ผ่านการทดสอบโดยจิตแพทย์ หรือนักจิตวิทยา ผ่านการตรวจจากแพทย์ทางต่อมไร้ท่อ การให้คำปรึกษาความพร้อมทางด้านจิตใจ เป็นต้น</p>
      <p> แพทย์ไทยที่ทำงานด้านนี้ต้องใช้ความประณีตมากเนื่องจากเป็นทั้งงานผ่าตัด และงานศิลปะ ในการเปลี่ยนอวัยวะเพศชายให้เป็นหญิงและร่วมเพศได้เช่นเดียวกับหญิงทั่วไป ผู้ที่ขอเข้ารับการผ่าตัดส่วนหนึ่งไม่ได้มีวัตถุประสงค์เกี่ยวกับเรื่องความ สุขทางเพศ แต่มีจุดมุ่งหมายอยากจะเป็นผู้หญิงที่สมบูรณ์ หลายคนยืนยันชัดเจนว่า ความปรารถนาสูงสุดคือ อยากตายในสภาพที่เป็นหญิง</p>
      <p>สำหรับผู้ชายไทยหรือกลุ่มคนเอเชียจะมีรูปหน้าไม่ต่างกับผู้หญิงมากนัก จึงไม่ต้องทำการผ่าตัดแปลงรูหน้าให้มากนัก แต่ถ้าเป็นฝรั่งหรือคนต่างชาติรูปหน้าดั้งเดิมจะมีความแตกต่างจากผู้หญิง จึงต้องมีการผ่าตัดแปลงรูปหน้าให้เป็นผู้หญิงด้วย (facial feminization)</p>
      <p>ปัจจุบันการผ่าตัดแปลงเพศได้พัฒนาไปอย่างมากซึ่งอาการแทรกซ้อนขณะที่ผ่า ตัดและหลังผ่าตัดมีน้อยมาก แต่หลังการผ่าตัดไปแล้วทุกคนยังต้องติดต่อกับแพทย์และให้ฮอร์โมนเสริมหลัง การผ่าตัดด้วย</p>
